package control.forus;

public class ForOutput {
	public static void main(String[] args) {
		//第一题输出数据1-10
		for(int a=1;a<=10;++a){
			System.out.println("a="+a);
		}
		System.out.println("______________________");
		//第二题输出数据10-1
		for(int a=10;a>=1;--a){
			System.out.println("a="+a);
		}
		
	}

}
