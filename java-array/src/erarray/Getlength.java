package erarray;
//获取数组的长度
public class Getlength {
    public static void main(String[] args){
        int arr[][] = {{23,43},{233,566},{452,123}};
        System.out.println(arr.length); //3 数组的长度
        System.out.println(arr[0].length);  //2 //数组第一个数组的长度
        System.out.println(arr[1].length);  //2 //数组第二个数组的长度
        System.out.println(arr[2].length);  //2 //数组第三个数组的长度
    }
}
